% 定义自变量x的观测值，单位可以是时间、温度等
x=[19     25    31     38    44]';

% 定义因变量y的观测值，对应x的响应值
y=[19.0   32.3   49.0   73.3   97.8]';

% 构建设计矩阵r，包含常数项1和x的平方项，用于二次多项式回归
% ones(5,1)创建5行1列的全1向量，x.^2计算x中每个元素的平方
r=[ones(5,1),x.^2];

% 使用最小二乘法求解回归系数ab
% ab(1)是截距项，ab(2)是二次项系数
ab = r\y

% 生成连续的x值用于绘制拟合曲线，从19到44，步长0.1
x0=19:0.1:44;

% 根据回归系数计算对应的y值，建立二次函数关系
y0=ab(1)+ab(2)*x0.^2;

% 绘制原始数据点和拟合曲线
% 'o-'表示原始数据用圆圈标记并连线，'g'表示拟合曲线用绿色
plot(x,y,'o-',x0,y0,'r')

% 输出回归分析结果
fprintf('二次多项式回归系数ab的结果:\n');
fprintf('a = %.4f\n', ab(1));
fprintf('b = %.4f\n', ab(2));
fprintf('拟合函数为: y = %.4f + %.4f * x^2\n', ab(1), ab(2));