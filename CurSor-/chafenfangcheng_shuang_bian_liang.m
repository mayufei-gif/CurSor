%{
 这是一个耦合的一阶线性差分方程组
具体特征：
1. 1.
阶数 ：一阶（只涉及k和k+1时刻）
2. 2.
线性性质 ：线性（变量关系都是一次的）
3. 3.
系数类型 ：常系数（a和b都是常数）
4. 4.
耦合关系 ：耦合系统（x和y相互依赖）
5. 5.
模型类型 ：类似于捕食者-猎物模型的离散版本
%}
% 差分方程迭代模型
% 模拟x和y两个变量的动态变化关系

% 初始参数设置
x0 = 100;          % x的初始参考值
y0 = 10;           % y的初始参考值
x = [110];         % x的初始实际值序列
y = [];            % y值序列（初始为空）
a = 0.1;           % y对x变化的敏感系数
b = 9;             % x对y变化的敏感系数
k_max = 10;        % 最大迭代次数

% 差分方程迭代计算
for k = 1:k_max
    % 根据当前x值计算y值：y_k = y0 - a*(x_k - x0)
    y_k = y0 - a*(x(k) - x0);
    y = [y, y_k];   % 将新计算的y值添加到序列
    
    % 如果不是最后一次迭代，计算下一个x值
    if k < k_max
        % 根据当前y值计算下一个x值：x_{k+1} = b*(y_k - y0)
        x_next = b*(y_k - y0);
        x = [x, x_next];  % 将新计算的x值添加到序列
    end
end

% 绘制结果
k = 1:k_max;
figure;

% 绘制x随k的变化关系
subplot(1,2,1);
plot(k, x, 'b-o', 'linewidth', 1.5);  % 修正了绘图格式
xlabel('k');
ylabel('x');
title('x与k的关系');
grid on;

% 绘制y随k的变化关系
subplot(1,2,2);
plot(k, y, 'b-o', 'linewidth', 1.5);  % 修正了绘图格式
xlabel('k');
ylabel('y');
title('y与k的关系');
grid on;
