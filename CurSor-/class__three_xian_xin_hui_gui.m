%{
 数据准备：

第一行clc,clear用于清除命令窗口和工作区，确保代码运行环境干净。
第二行创建了一个列向量x，包含25个连续的整数（从143到167）。
第三行x=[ones(25,1) x]为x添加了一列1，这是为了在回归模型中包含截距项。这样设计后，x变成了一个25×2的矩阵，第一列全为1，第二列是原始的x值。
第四行创建了y的列向量，包含25个连续的整数（从85到106）。
回归分析：

第五行[b, bint,r,rint,stats]=regress(y,x)执行线性回归分析，返回多个结果：
b: 回归系数向量，包括截距和斜率
bint: 回归系数的置信区间
r: 残差向量
rint: 残差的置信区间
stats: 回归统计量，包括R²、F统计量、p值和误差方差
结果显示：
最后三行分别显示回归系数b、置信区间bint和统计量stats。
%}
