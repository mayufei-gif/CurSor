% 定义自变量x的观测值
x=[19     25    31     38    44]';

% 定义因变量y的观测值
y=[19.0   32.3   49.0   73.3   97.8]';

% 构建三次多项式设计矩阵，包含常数项、x、x²和x³
% [1, x, x², x³]
r=[ones(5,1), x, x.^2, x.^3];

% 使用最小二乘法求解三次多项式回归系数
% ab = [a, b, c, d]，对应：y = a + b·x + c·x² + d·x³
ab=r\y

% 生成连续的x值用于绘制拟合曲线
x0=19:0.1:44;

% 根据三次多项式系数计算对应的y值
y0=ab(1)+ab(2)*x0+ab(3)*x0.^2+ab(4)*x0.^3;

% 绘制原始数据点和三次多项式拟合曲线
plot(x,y,'o-',x0,y0,'g')

% 输出三次多项式回归分析结果
fprintf('三次多项式回归系数ab的结果:\n');
fprintf('a = %.4f\n', ab(1));
fprintf('b = %.4f\n', ab(2));
fprintf('c = %.4f\n', ab(3));
fprintf('d = %.4f\n', ab(4));
fprintf('拟合函数为: y = %.4f + %.4f·x + %.4f·x² + %.4f·x³\n', ab(1), ab(2), ab(3), ab(4));
